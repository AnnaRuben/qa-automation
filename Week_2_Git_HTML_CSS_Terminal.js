console.log("git init                 → יוצר ריפוזיטורי חדש מקומי בתיקייה הנוכחית");
console.log("git status               → מציג את מצב הקבצים (שינויים, קבצים שלא מנוהלים)");
console.log("git add <filename>       → מוסיף קובץ ספציפי ל-staging");
console.log("git add .                → מוסיף את כל הקבצים ששונו ל-staging");
console.log("git commit -m \"message\"   → יוצר גרסה (commit) עם הודעת תיעוד");
console.log("git log                  → מציג היסטוריית קומיטים");
console.log("git branch               → מציג רשימת ענפים מקומיים");
console.log("git branch <new-branch>  → יוצר ענף חדש");
console.log("git checkout <branch>    → מעבר לענף קיים");
console.log("git checkout -b <branch> → יוצר ענף חדש ועובר אליו מיד");
console.log("git merge <branch>       → ממזג ענף לתוך הענף הנוכחי");
console.log("git remote add origin <repo-url> → מחבר את הפרויקט המקומי לריפו ב-GitHub");
console.log("git push -u origin main  → דחיפה ראשונה של הענף הראשי ל-GitHub");
console.log("git push                 → דחיפת עדכונים ל-GitHub");
console.log("git pull                 → משיכת עדכונים מ-GitHub לענף הנוכחי");
console.log("git clone <repo-url>     → הורדת ריפו קיים מ-GitHub למחשב מקומי");


// ===============================
// 🟠 פקודות טרמינל כלליות (HTML/CSS)
// ===============================

console.log("pwd                 → מציג את הנתיב הנוכחי (Print Working Directory)");
console.log("ls                  → מציג קבצים ותיקיות בתיקייה הנוכחית");
console.log("cd <path>           → מעבר לתיקייה (Change Directory)");
console.log("touch index.html    → יצירת קובץ HTML חדש");
console.log("touch style.css     → יצירת קובץ CSS חדש");
console.log("mkdir <foldername>  → יצירת תיקייה חדשה (לדוגמה images)");
console.log("code .              → פותח את התיקייה הנוכחית ב-VS Code");
console.log("open index.html     → פתיחת קובץ ה-HTML בדפדפן (macOS)");
console.log("start index.html    → פתיחת קובץ ה-HTML בדפדפן (Windows)");
console.log("clear               → ניקוי המסך בטרמינל");
