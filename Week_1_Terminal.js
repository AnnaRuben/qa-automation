// ===============================
// 📂 ניווט וקבצים בסיסיים
// ===============================

console.log("pwd   → מציג את הנתיב הנוכחי (Print Working Directory)");
console.log("ls    → מציג קבצים ותיקיות בתיקייה הנוכחית");
console.log("ls -l → מציג פירוט מורחב (הרשאות, גודל, תאריכים)");
console.log("ls -a → מציג גם קבצים מוסתרים (מתחילים ב-.)");
console.log("cd <path> → מעבר לתיקייה (Change Directory)");
console.log("cd .. → חזרה תיקייה אחת אחורה");
console.log("cd ~  → מעבר ישירות לתיקיית הבית (Home)");


// ===============================
// 📄 יצירת קבצים ותיקיות
// ===============================

console.log("touch <filename>  → יוצר קובץ חדש ריק");
console.log("mkdir <foldername> → יוצר תיקייה חדשה");


// ===============================
// ✍️ עבודה עם תוכן
// ===============================

console.log("echo 'טקסט' > file.txt  → יוצר/מחליף תוכן בקובץ");
console.log("echo 'טקסט' >> file.txt → מוסיף טקסט לסוף קובץ קיים");
console.log("cat file.txt           → מציג את תוכן הקובץ במסך");


// ===============================
// 🗑️ מחיקה
// ===============================

console.log("rm <filename>      → מוחק קובץ");
console.log("rmdir <foldername> → מוחק תיקייה ריקה בלבד");
console.log("rm -r <foldername> → מוחק תיקייה עם כל התוכן (Recursive)");


// ===============================
// ⚡ כללי
// ===============================

console.log("clear            → מנקה את המסך בטרמינל");
console.log("node filename.js → מריץ קובץ JavaScript באמצעות Node.js");